public class Sum {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        int sum = a + b;
        System.out.println("The sum of " + a + " and " + b + " is: " + sum);
        int diff = a - b;
        System.out.println("The difference of " + a + " and " + b + " is: " + diff);
    }
    
}
